		"for (const [id, name] of Object.entries(data)){"
			"s += \"<option value='\" + id + \"'>\" + name + \"</option>\";"
		"}"
		"tagselect.html(s);"
		"tagselect.select2();" // Initialise it
	"});"
"}"
