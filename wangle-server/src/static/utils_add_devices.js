"function add_device(){"
	"const queue = document.getElementById('add-devices-queue');"
	"const urls = Array.from(queue.getElementsByTagName('ul')).map(x => x.textContent);"
	"if(urls.length===0)"
		"return;"
	"const protocol = document.getElementById(\"protocolselect\").value;"
	"if(protocol === \"\"){"
		"alert(\"Please set the protocol\");"
		"return;"
	"}"
	"$.ajax({"
		"type:\"POST\","
		"url:\"http://localhost:1999/add-D/\" + protocol,"
		"data:urls.join(\"\\n\"),"
		"success:function(){"
			"queue.innerHTML = \"\";" // Remove URLs
			"alert(\"Success\");"
			"refetch_json('D', '/a/D.json');"
		"},"
		"error:err_alert,"
		"dataType:\"text\""
	"});"
"}"

"function add_devices__append(){"
	"const inp = document.getElementById('add-device-input');"
	"const x = inp.value;"
	"if(x !== \"\")"
		"document.getElementById('add-devices-queue').innerHTML += \"<ul>\" + inp.value + \"</ul>\";"
	"inp.value = \"\";"
"}"
